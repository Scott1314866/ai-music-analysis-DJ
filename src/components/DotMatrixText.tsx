interface DotMatrixTextProps {
  text: string;
  dotSizePx?: number;
  gapPx?: number;
  activeColorClass?: string;
  inactiveColorClass?: string;
  scale?: number;
}

export function DotMatrixText({
  text,
  dotSizePx = 6,
  gapPx = 2,
  activeColorClass = 'bg-[#3fb950] shadow-[0_0_8px_#3fb950]',
  inactiveColorClass = 'bg-[#ffffff]/5',
  scale = 1
}: DotMatrixTextProps) {
  const chars = text.toUpperCase().split('');

  return (
    <div className="flex gap-4 select-none overflow-x-auto py-1" style={{ transform: `scale(${scale})`, transformOrigin: 'left center' }}>
      {chars.map((char, charIdx) => {
        const matrix = DOT_FONT[char] || DOT_FONT[' '];
        return (
          <div key={charIdx} className="grid grid-cols-5 shrink-0" style={{ gap: `${gapPx}px` }}>
            {matrix.map((row, rowIdx) =>
              row.map((active, colIdx) => (
                <div
                  key={`${rowIdx}-${colIdx}`}
                  className={`rounded-full transition-all duration-300 ${
                    active === 1 ? activeColorClass : inactiveColorClass
                  }`}
                  style={{
                    width: `${dotSizePx}px`,
                    height: `${dotSizePx}px`,
                  }}
                />
              ))
            )}
          </div>
        );
      })}
    </div>
  );
}
